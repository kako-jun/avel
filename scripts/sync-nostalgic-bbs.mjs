import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";

const CONTENT_DIR = "content/posts";
const DATA_FILE = "data/nostalgic_bbs.toml";
const DEFAULT_API_BASE = "https://api.nostalgic.llll-ll.com";
const BATCH_LIMIT = 50;

const token = process.env.NOSTALGIC_TOKEN || "";
const apiBase = (process.env.NOSTALGIC_API_BASE || DEFAULT_API_BASE).replace(/\/$/, "");

function normalizePath(value) {
  const trimmed = value.trim().replace(/^\/+|\/+$/g, "");
  return `/${trimmed}/`;
}

function parseScalar(frontmatter, key) {
  const match = frontmatter.match(new RegExp(`^${key}\\s*=\\s*["']?([^"'\\n]+)["']?\\s*$`, "m"));
  return match ? match[1].trim() : "";
}

function parseBool(frontmatter, key) {
  return new RegExp(`^${key}\\s*=\\s*true\\s*$`, "m").test(frontmatter);
}

function parseExtraBbsId(frontmatter) {
  const extraStart = frontmatter.search(/^\[extra\]\s*$/m);
  if (extraStart === -1) return "";
  const rest = frontmatter.slice(extraStart);
  const nextSection = rest.slice(1).search(/^\[[^\]]+\]\s*$/m);
  const extra = nextSection === -1 ? rest : rest.slice(0, nextSection + 1);
  return parseScalar(extra, "nostalgic_bbs_id");
}

async function walkMarkdown(dir) {
  const files = [];
  for (const entry of await readdir(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await walkMarkdown(fullPath)));
    } else if (entry.isFile() && entry.name.endsWith(".md") && entry.name !== "_index.md") {
      files.push(fullPath);
    }
  }
  return files;
}

function extractFrontmatter(source) {
  if (!source.startsWith("+++\n")) return "";
  const end = source.indexOf("\n+++", 4);
  return end === -1 ? "" : source.slice(4, end);
}

function pagePathForFile(file, frontmatter) {
  const explicitPath = parseScalar(frontmatter, "path");
  if (explicitPath) return normalizePath(explicitPath);

  const rel = path.relative("content", file).replace(/\\/g, "/").replace(/\.md$/, "");
  const slug = parseScalar(frontmatter, "slug");
  if (!slug) return normalizePath(rel);

  const parts = rel.split("/");
  parts[parts.length - 1] = slug;
  return normalizePath(parts.join("/"));
}

function readBaseUrl(configToml) {
  const baseUrl = parseScalar(configToml, "base_url");
  if (!baseUrl) throw new Error("config.toml must define base_url");
  return baseUrl.replace(/\/$/, "");
}

async function readExistingMap() {
  if (!existsSync(DATA_FILE)) return {};
  const source = await readFile(DATA_FILE, "utf8");
  const map = {};
  let inPosts = false;
  for (const line of source.split(/\r?\n/)) {
    if (/^\[posts\]\s*$/.test(line)) {
      inPosts = true;
      continue;
    }
    if (/^\[/.test(line)) inPosts = false;
    if (!inPosts) continue;
    const match = line.match(/^"([^"]+)"\s*=\s*"([^"]+)"\s*$/);
    if (match) map[match[1]] = match[2];
  }
  return map;
}

function toToml(map) {
  const lines = ["# Generated by scripts/sync-nostalgic-bbs.mjs", "# Keyed by Zola page.path.", "", "[posts]"];
  for (const key of Object.keys(map).sort()) {
    lines.push(`${JSON.stringify(key)} = ${JSON.stringify(map[key])}`);
  }
  return `${lines.join("\n")}\n`;
}

async function postJson(action, body) {
  const response = await fetch(`${apiBase}/bbs?action=${action}`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  const json = await response.json().catch(() => ({}));
  return { ok: response.ok, status: response.status, json };
}

async function lookupOrCreateBbs(url) {
  const lookup = await postJson("get", { url, token, limit: 1 });
  if (lookup.ok && lookup.json?.success && lookup.json?.data?.id) {
    return lookup.json.data.id;
  }

  if (lookup.status !== 404 && lookup.json?.error !== "BBS not found") {
    throw new Error(`BBS lookup failed for ${url}: ${lookup.status} ${lookup.json?.error || ""}`.trim());
  }

  const created = await postJson("create", {
    url,
    token,
    title: "Comments",
    maxMessages: 100,
    messagesPerPage: 20,
  });
  if (!created.ok || !created.json?.success || !created.json?.id) {
    throw new Error(`BBS create failed for ${url}: ${created.status} ${created.json?.error || ""}`.trim());
  }
  return created.json.id;
}

async function main() {
  const configToml = await readFile("config.toml", "utf8");
  const baseUrl = readBaseUrl(configToml);
  const existing = await readExistingMap();
  const next = { ...existing };
  const files = existsSync(CONTENT_DIR) ? await walkMarkdown(CONTENT_DIR) : [];

  let skippedManual = 0;
  const missing = [];
  for (const file of files) {
    const source = await readFile(file, "utf8");
    const frontmatter = extractFrontmatter(source);
    if (!frontmatter || parseBool(frontmatter, "draft")) continue;
    if (parseExtraBbsId(frontmatter)) {
      skippedManual += 1;
      continue;
    }
    const pagePath = pagePathForFile(file, frontmatter);
    if (next[pagePath]) continue;
    missing.push({ pagePath, url: `${baseUrl}${pagePath}` });
  }

  if (!token) {
    console.log(
      `NOSTALGIC_TOKEN is not set; kept existing ${Object.keys(existing).length} BBS ids, skipped ${missing.length} missing posts.`
    );
    return;
  }

  // Nostalgic BBS does not expose batchGet yet. If it grows a URL-based batch owner lookup,
  // use BATCH_LIMIT-sized chunks here; D1 bind variables are limited to 100.
  for (let i = 0; i < missing.length; i += BATCH_LIMIT) {
    for (const item of missing.slice(i, i + BATCH_LIMIT)) {
      next[item.pagePath] = await lookupOrCreateBbs(item.url);
      console.log(`${item.pagePath} -> ${next[item.pagePath]}`);
    }
  }

  await mkdir(path.dirname(DATA_FILE), { recursive: true });
  await writeFile(DATA_FILE, toToml(next));
  console.log(
    `Wrote ${DATA_FILE}: ${Object.keys(next).length} ids (${missing.length} new, ${skippedManual} manual).`
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
